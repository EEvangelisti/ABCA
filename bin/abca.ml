open Abca

type mode =
  | Run
  | Xml
  | Render

let mode_of_string = function
  | "run" -> Run
  | "xml" -> Xml
  | "render" -> Render
  | s -> invalid_arg ("Unknown mode: " ^ s)

let timestamp () =
  let tm =
    Unix.localtime (Unix.time ())
  in

  Printf.sprintf
    "%04d-%02d-%02d_%02d%02d%02d"
    (tm.Unix.tm_year + 1900)
    (tm.Unix.tm_mon + 1)
    tm.Unix.tm_mday
    tm.Unix.tm_hour
    tm.Unix.tm_min
    tm.Unix.tm_sec

let ensure_dir dirname =
  if Sys.file_exists dirname then begin
    if not (Sys.is_directory dirname) then
      invalid_arg ("Not a directory: " ^ dirname)
  end else
    Unix.mkdir dirname 0o755

let path_join =
  Filename.concat


let () =
  let list_models = ref false in
  let mode = ref "run" in
  let model = ref "life" in
  let rows = ref 200 in
  let cols = ref 200 in
  let generations = ref 100 in
  let seed = ref 1 in
  let density = ref 0.25 in
  let toroidal = ref false in
  let out = ref "simulation.autbin" in
  let input = ref "" in
  let xml = ref "simulation.xml" in
  let png = ref "" in
  let gif = ref "" in
  let mp4 = ref "" in
  let fps = ref 30 in
  let every = ref 1 in
  let cell_size = ref 3 in
  let prefix = ref "life" in
  let palette = ref "grayscale" in
  let palette_states = ref 256 in
  let render_root = ref "output" in
  let render_name = ref "" in
  let ffmpeg_log = ref true in
  let background = ref "" in
  let skip_background = ref true in
  let agents = ref 0 in

  let specs = Arg.align [
    "--mode", Arg.Set_string mode, " run | xml";
    "--model", Arg.Set_string model, " Model name";
    "--rows", Arg.Set_int rows, " Number of rows";
    "--cols", Arg.Set_int cols, " Number of columns";
    "--generations", Arg.Set_int generations, " Number of generations";
    "--seed", Arg.Set_int seed, " Random seed";
    "--density", Arg.Set_float density, " Initial density";
    "--toroidal", Arg.Set toroidal, " Use toroidal topology";
    "--out", Arg.Set_string out, " Binary output file";
    "--input", Arg.Set_string input, " Binary input file";
    "--xml", Arg.Set_string xml, " XML output file";
    "--png", Arg.Set_string png, " Output PNG directory";
    "--gif", Arg.Set_string gif, " Output animated GIF file";
    "--mp4", Arg.Set_string mp4, " Output MP4 file";
    "--fps", Arg.Set_int fps, " Animation frame rate";
    "--every", Arg.Set_int every, " Render one frame every N generations";
    "--cell-size", Arg.Set_int cell_size, " Cell size in pixels";
    "--prefix", Arg.Set_string prefix, " PNG filename prefix";
    "--list-models", Arg.Set list_models, " List available simulation models";
    "--palette", Arg.Set_string palette, " Rendering palette";
    "--palette-states", Arg.Set_int palette_states, " Number of colors generated by the palette";
    "--render-root", Arg.Set_string render_root, "Root directory for render outputs";
    "--render-name", Arg.Set_string render_name, "Name of the render output directory";
    "--ffmpeg-log", Arg.Set ffmpeg_log, "Write ffmpeg output to ffmpeg.log";
    "--no-ffmpeg-log", Arg.Clear ffmpeg_log, "Discard ffmpeg output";
    "--background", Arg.Set_string background, " Background color name or #RRGGBB";
    "--skip-background", Arg.Set skip_background, "Do not draw cells with color index 0";
    "--draw-background", Arg.Clear skip_background, "Draw cells with color index 0";
    "--agents", Arg.Set_int agents, "Number of agents for agent-based models";
  ] in

  Arg.parse specs
    (fun s -> raise (Arg.Bad ("Unexpected argument: " ^ s)))
    "abca — command-line cellular automata engine";

  Abca_plugin_registry.Register_plugins.initialize ();
  Abca_palette.Builtin.initialize ();

  if !list_models then begin

    let open Abca_models in

    let current_family = ref None in

    Registry.all ()
    |> List.iter (fun model ->

        if !current_family <> Some model.Model.family then begin
          current_family := Some model.Model.family;
          Printf.printf "\n%s\n"
            (Model.family_to_string model.Model.family)
        end;

        Printf.printf
          "  %-20s %s\n"
          model.Model.name
          model.Model.description);

    print_newline ();
    exit 0
  end;

  let selected_model =
    try Abca_models.Registry.find !model
    with Abca_models.Registry.Unknown_model name ->
      Printf.eprintf "Unknown model: %s\n" name;
      Printf.eprintf "Available models:\n";
      Abca_models.Registry.all ()
      |> List.iter (fun m ->
          Printf.eprintf "  - %s\n" m.Abca_models.Model.name);
      exit 1
  in

  match mode_of_string !mode with
  | Run ->
      let topology =
        if !toroidal then Grid.Toroidal else Grid.Bounded
      in

      selected_model.Abca_models.Model.run
        ~rows:!rows
        ~cols:!cols
        ~generations:!generations
        ~seed:!seed
        ~density:!density
        ~agents:(if !agents <= 0 then None else Some !agents)
        ~topology
        ~output:!out;

      Printf.printf
        "Saved %s: %d x %d, %d generations -> %s\n%!"
        selected_model.Abca_models.Model.name
        !rows
        !cols
        !generations
        !out

  | Xml ->
      if !input = "" then begin
        prerr_endline "--input is required in XML mode";
        exit 1
      end;

      selected_model.Abca_models.Model.export_xml
        ~input:!input
        ~output:!xml;

      Printf.printf "Exported XML -> %s\n%!" !xml

  | Render ->
      if !input = "" then begin
        prerr_endline "--input is required in render mode";
        exit 1
      end;

      if !png = "" && !gif = "" && !mp4 = "" then begin
        prerr_endline "Nothing to render: use --png, --gif, or --mp4";
        exit 1
      end;

      let base_name =
        if !render_name <> "" then
          !render_name
        else
          Printf.sprintf "%s_%s"
            (timestamp ())
            selected_model.Abca_models.Model.name
      in

      ensure_dir !render_root;

      let render_dir =
        path_join !render_root base_name
      in

      ensure_dir render_dir;

      let png_dir =
        if !png <> "" then
          path_join render_dir !png
        else
          path_join render_dir "frames"
      in

      let gif_output =
        if !gif = "" then
          None
        else
          Some (path_join render_dir !gif)
      in

      let mp4_output =
        if !mp4 = "" then
          None
        else
          Some (path_join render_dir !mp4)
      in

      let log_file =
        if !ffmpeg_log then
          Some (path_join render_dir "ffmpeg.log")
        else
          None
      in

      let _header, frames, _agents =
        Abca_io.Binary.load_frames
          ~filename:!input
          ~codec:(module struct
            type t = int

            let to_int32 x =
              Int32.of_int x

            let of_int32 x =
              Int32.to_int x
          end)
      in

      let palette_generator =
        Abca_palette.Register.get !palette
      in

      let render_palette =
        palette_generator.Abca_palette.Register.make
          ~states:selected_model.Abca_models.Model.state_count
      in

      let background_color =
        if !background = "" then
          None
        else
          Some (Abca_palette.Color.parse !background)
      in

      let skip_index =
        if !skip_background then Some 0 else None
      in

      Abca_render.Png.save_frames
        ~dirname:png_dir
        ~prefix:!prefix
        ~every:!every
        ~cell_size:!cell_size
        ?background:background_color
        ?skip_index
        ~palette:render_palette
        ~to_color_index:selected_model.Abca_models.Model.to_color_index
        frames;

      Option.iter
        (fun output ->
           Abca_render.Video.make_gif
             ?log_file
             ~fps:!fps
             ~png_dir
             ~prefix:!prefix
             ~output ())
        gif_output;

      Option.iter
        (fun output ->
           Abca_render.Video.make_mp4
             ?log_file
             ~fps:!fps
             ~png_dir
             ~prefix:!prefix
             ~output ())
        mp4_output;

      Printf.printf "Render directory -> %s\n%!" render_dir
