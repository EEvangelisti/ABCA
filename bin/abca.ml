open Abca

module Mode = struct
  type t =
    | Run
    | Xml
    | Render
    | Analyze

  let all = [
    "run", Run;
    "xml", Xml;
    "render", Render;
    "analyze", Analyze;
  ]

  let of_string s =
    match List.assoc_opt (String.lowercase_ascii s) all with
    | Some mode -> mode
    | None -> invalid_arg ("Unknown mode: " ^ s)

  let requires_model = function
    | Run | Xml | Render -> true
    | Analyze -> false

  let help =
    all
    |> List.map fst
    |> String.concat " | "
end


let timestamp () =
  let tm =
    Unix.localtime (Unix.time ())
  in
  Printf.sprintf
    "%04d-%02d-%02d_%02d%02d%02d"
    (tm.Unix.tm_year + 1900)
    (tm.Unix.tm_mon + 1)
    tm.Unix.tm_mday
    tm.Unix.tm_hour
    tm.Unix.tm_min
    tm.Unix.tm_sec

let ensure_dir dirname =
  if Sys.file_exists dirname then begin
    if not (Sys.is_directory dirname) then
      invalid_arg ("Not a directory: " ^ dirname)
  end else
    Unix.mkdir dirname 0o755


let export_agent_trace_csv ~filename (agents : Abca_io.Agent_trace.t) =
  let oc = open_out filename in
  Fun.protect
    ~finally:(fun () -> close_out_noerr oc)
    (fun () ->
       output_string oc "frame,id,row,col,angle,age,state\n";
       Array.iter
         (fun r ->
            Printf.fprintf oc "%d,%d,%d,%d,%d,%d,%d\n"
              r.Abca_io.Agent_trace.frame
              r.id
              r.row
              r.col
              r.angle
              r.age
              r.state)
         agents)


module Settings =
  struct
    let list_models = ref false
    let mode = ref "run"
    let model = ref "life"
    let rows = ref 200
    let cols = ref 200
    let generations = ref 100
    let seed = ref 1
    let density = ref 0.25
    let toroidal = ref false
    let out = ref "simulation.autbin"
    let input = ref ""
    let xml = ref "simulation.xml"
    let png = ref ""
    let gif = ref ""
    let mp4 = ref ""
    let fps = ref 30
    let every = ref 1
    let cell_size = ref 3
    let palette = ref "grayscale"
    let palette_states = ref 256
    let render_root = ref "output"
    let render_name = ref ""
    let ffmpeg_log = ref true
    let background = ref ""
    let skip_background = ref true
    let agents = ref 0
    let csv = ref "trajectories.csv"
    let tracking_xml = ref ""
    let plugin_args = ref []

    open Arg
    let spec_list = align [
      "--mode", Set_string mode, " " ^ Mode.help;
      "--model", Set_string model, " Model name";
      "--rows", Set_int rows, " Number of rows";
      "--cols", Set_int cols, " Number of columns";
      "--generations", Set_int generations, " Number of generations";
      "--seed", Set_int seed, " Random seed";
      "--density", Set_float density, " Initial density";
      "--toroidal", Set toroidal, " Use toroidal topology";
      "--out", Set_string out, " Binary output file";
      "--input", Set_string input, " Binary input file";
      "--xml", Set_string xml, " XML output file";
      "--png", Set_string png, " Output PNG directory";
      "--gif", Set_string gif, " Output animated GIF file";
      "--mp4", Set_string mp4, " Output MP4 file";
      "--fps", Set_int fps, " Animation frame rate";
      "--every", Set_int every, " Render one frame every N generations";
      "--cell-size", Set_int cell_size, " Cell size in pixels";
      "--list-models", Set list_models, " List available simulation models";
      "--palette", Set_string palette, " Rendering palette";
      "--palette-states", Set_int palette_states, " Number of colors generated by the palette";
      "--render-root", Set_string render_root, "Root directory for render outputs";
      "--render-name", Set_string render_name, "Name of the render output directory";
      "--ffmpeg-log", Set ffmpeg_log, "Write ffmpeg output to ffmpeg.log";
      "--no-ffmpeg-log", Clear ffmpeg_log, "Discard ffmpeg output";
      "--background", Set_string background, " Background color name or #RRGGBB";
      "--skip-background", Set skip_background, "Do not draw cells with color index 0";
      "--draw-background", Clear skip_background, "Draw cells with color index 0";
      "--agents", Set_int agents, "Number of agents for agent-based models";
      "--csv", Set_string csv, " CSV output file for analysis mode";
      "--tracking-xml", Set_string tracking_xml, " XML particle-tracking output file for analysis mode";
      "--plugin-arg",
      Arg.String
        (fun s ->
           match String.split_on_char '=' s with
           | [ key; value ] ->
               plugin_args := (String.uppercase_ascii key, value) :: !plugin_args
           | _ ->
               invalid_arg "--plugin-arg expects KEY=VALUE"),
      " Override a plugin parameter, e.g. --plugin-arg RADIUS=35";
    ]

    let ensure_input () =
      if !input = "" then begin
        prerr_endline ("--input is required in " ^ !mode ^ " mode");
        exit 1
      end
  end


module Action =
  struct
    open Settings
    let run selected_model =
      let topology =
        if !toroidal then Grid.Toroidal else Grid.Bounded
      in
      selected_model.Abca_models.Model.run
        ~rows:!rows
        ~cols:!cols
        ~generations:!generations
        ~seed:!seed
        ~density:!density
        ~agents:(if !agents <= 0 then None else Some !agents)
        ~topology
        ~plugin_args:(List.rev !plugin_args)
        ~output:!out;
      Printf.printf
      "Saved %s: %d x %d, %d generations -> %s\n%!"
      selected_model.Abca_models.Model.name
      !rows
      !cols
      !generations
      !out


    let xml selected_model =
      ensure_input ();
      selected_model.Abca_models.Model.export_xml
        ~input:!input
        ~output:!xml;
      Printf.printf "Exported XML -> %s\n%!" !xml


    let analyze () =
      ensure_input ();
      let module Binary_codec = struct
        type t = int
        let to_int32 = Int32.of_int
        let of_int32 = Int32.to_int
      end in
      let open Abca_io.Binary in
      let { agents; _ } =
        load
          ~filename:!input
          ~codec:(module Binary_codec)
      in
      export_agent_trace_csv
        ~filename:!csv
        agents;
      Printf.printf "Exported agent trajectories -> %s\n%!" !csv;
      if !tracking_xml <> "" then begin
        Abca_io.Xml.save_agent_trace_trackmate
          ~filename:!tracking_xml
          agents;

        Printf.printf
          "Exported particle-tracking XML -> %s\n%!"
          !tracking_xml
      end



    let render selected_model =
      ensure_input ();

      if !png = "" && !gif = "" && !mp4 = "" then begin
        prerr_endline "Nothing to render: use --png, --gif, or --mp4";
        exit 1
      end;

      let base_name =
        if !render_name <> "" then
          !render_name
        else
          Printf.sprintf "%s_%s"
            (timestamp ())
            selected_model.Abca_models.Model.name
      in

      ensure_dir !render_root;

      let render_dir =
        Filename.concat !render_root base_name
      in

      ensure_dir render_dir;

      let png_dir =
        if !png <> "" then
          Filename.concat render_dir !png
        else
          Filename.concat render_dir "frames"
      in

      let gif_output =
        if !gif = "" then
          None
        else
          Some (Filename.concat render_dir !gif)
      in

      let mp4_output =
        if !mp4 = "" then
          None
        else
          Some (Filename.concat render_dir !mp4)
      in

      let log_file =
        if !ffmpeg_log then
          Some (Filename.concat render_dir "ffmpeg.log")
        else
          None
      in

      let module Binary_codec =
        struct
          type t = int
          let to_int32 = Int32.of_int
          let of_int32 = Int32.to_int
        end
      in

      let open Abca_io.Binary in
      let { frames; _ } =
        load
          ~filename:!input
          ~codec:(module Binary_codec)
      in
      let palette_generator = Abca_palette.Register.get !palette in
      let render_palette =
        palette_generator.Abca_palette.Register.make
          ~states:selected_model.Abca_models.Model.state_count
      in
      let background_color =
        if !background = "" then
          None
        else
          Some (Abca_palette.Color.parse !background)
      in
      Abca_render.Png.save_frames
        ~dirname:png_dir
        ~prefix:!model
        ~every:!every
        ~cell_size:!cell_size
        ?background:background_color
        ~palette:render_palette
        ~to_color_index:selected_model.Abca_models.Model.to_color_index
        frames;
      Option.iter
        (fun output ->
           Abca_render.Video.make_gif
             ?log_file
             ~fps:!fps
             ~png_dir
             ~prefix:!model
             ~output ())
        gif_output;
      Option.iter
        (fun output ->
           Abca_render.Video.make_mp4
             ?log_file
             ~fps:!fps
             ~png_dir
             ~prefix:!model
             ~output ())
        mp4_output;
      Printf.printf "Render directory -> %s\n%!" render_dir
      
    let choose mode selected_model =
      match mode, selected_model with
      | Mode.Run, Some model -> run model
      | Mode.Xml, Some model -> xml model
      | Mode.Render, Some model -> render model
      | Mode.Analyze, _ -> analyze ()
      | _, None -> invalid_arg "This mode requires a model"
  end



let () =

  let open Settings in

  Arg.parse spec_list
    (fun s -> raise (Arg.Bad ("Unexpected argument: " ^ s)))
    "abca — command-line cellular automata engine";

  Abca_plugin_registry.Register_plugins.initialize ();
  Abca_palette.Builtin.initialize ();

  if !list_models then begin

    let open Abca_models in

    let current_family = ref None in

    Registry.all ()
    |> List.iter (fun model ->

        if !current_family <> Some model.Model.family then begin
          current_family := Some model.Model.family;
          Printf.printf "\n%s\n"
            (Model.family_to_string model.Model.family)
        end;

        Printf.printf
          "  %-20s %s\n"
          model.Model.name
          model.Model.description);

    print_newline ();
    exit 0
  end;

  let selected_mode = Mode.of_string !mode in

  let selected_model =
    if Mode.requires_model selected_mode then
      try Some (Abca_models.Registry.find !model)
      with Abca_models.Registry.Unknown_model name ->
        Printf.eprintf "Unknown model: %s\n" name;
        Printf.eprintf "Available models:\n";
        Abca_models.Registry.all ()
        |> List.iter (fun m ->
            Printf.eprintf "  - %s\n" m.Abca_models.Model.name);
        exit 1
    else
      None
  in

  Action.choose selected_mode selected_model
  
